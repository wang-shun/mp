package com.fiberhome.mapps.ydzf.entity;

public class LawSamplingList {
	private String id;
	
	private String declNo;
	
	private String samplingId;
	
	private String goodsItemName;
	
	private String specification;
	
	private String quantity;
	
	private String prodHsCode;
	
	private String oriCity;
	
	private String markNumber;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDeclNo() {
		return declNo;
	}

	public void setDeclNo(String declNo) {
		this.declNo = declNo;
	}

	public String getSamplingId() {
		return samplingId;
	}

	public void setSamplingId(String samplingId) {
		this.samplingId = samplingId;
	}

	public String getGoodsItemName() {
		return goodsItemName;
	}

	public void setGoodsItemName(String goodsItemName) {
		this.goodsItemName = goodsItemName;
	}

	public String getSpecification() {
		return specification;
	}

	public void setSpecification(String specification) {
		this.specification = specification;
	}

	public String getQuantity() {
		return quantity;
	}

	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}

	public String getProdHsCode() {
		return prodHsCode;
	}

	public void setProdHsCode(String prodHsCode) {
		this.prodHsCode = prodHsCode;
	}

	public String getOriCity() {
		return oriCity;
	}

	public void setOriCity(String oriCity) {
		this.oriCity = oriCity;
	}

	public String getMarkNumber() {
		return markNumber;
	}

	public void setMarkNumber(String markNumber) {
		this.markNumber = markNumber;
	}
}
