package com.fiberhome.mapps.szzj.entity;

public class ChannelUser {
	private String user_id;
	
	private String email;
	
	private String type;
	
	private String meetings;
	
	private String participants;
	
	private String meeting_minutes;
	
	private String last_client_version;
	
	private String last_login_time;

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getMeetings() {
		return meetings;
	}

	public void setMeetings(String meetings) {
		this.meetings = meetings;
	}

	public String getParticipants() {
		return participants;
	}

	public void setParticipants(String participants) {
		this.participants = participants;
	}

	public String getMeeting_minutes() {
		return meeting_minutes;
	}

	public void setMeeting_minutes(String meeting_minutes) {
		this.meeting_minutes = meeting_minutes;
	}

	public String getLast_client_version() {
		return last_client_version;
	}

	public void setLast_client_version(String last_client_version) {
		this.last_client_version = last_client_version;
	}

	public String getLast_login_time() {
		return last_login_time;
	}

	public void setLast_login_time(String last_login_time) {
		this.last_login_time = last_login_time;
	}
	
	
}
