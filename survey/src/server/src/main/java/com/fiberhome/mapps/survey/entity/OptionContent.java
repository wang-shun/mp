package com.fiberhome.mapps.survey.entity;

public class OptionContent
{

    private String optionId;

    private String optionContent;

    private Long   optionSequ;

    private String customInput;

    private String optionSelect;

    public String getOptionId()
    {
        return optionId;
    }

    public void setOptionId(String optionId)
    {
        this.optionId = optionId;
    }

    public String getOptionContent()
    {
        return optionContent;
    }

    public void setOptionContent(String optionContent)
    {
        this.optionContent = optionContent;
    }

    public Long getOptionSequ()
    {
        return optionSequ;
    }

    public void setOptionSequ(Long optionSequ)
    {
        this.optionSequ = optionSequ;
    }

    public String getOptionSelect()
    {
        return optionSelect;
    }

    public void setOptionSelect(String optionSelect)
    {
        this.optionSelect = optionSelect;
    }

    public String getCustomInput()
    {
        return customInput;
    }

    public void setCustomInput(String customInput)
    {
        this.customInput = customInput;
    }

}
