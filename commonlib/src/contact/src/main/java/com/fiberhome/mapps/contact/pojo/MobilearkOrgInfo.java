package com.fiberhome.mapps.contact.pojo;

public class MobilearkOrgInfo
{
    private String  orgUuid;
    private String  orgCode;
    private String  orgName;
    private Integer userNum;
    private Integer deviceNum;
    private Integer exmobiAppNum;
    private Integer licenseNum;
    private Integer usedLicenseNum;
    private Integer orgStatus;

    public String getOrgUuid()
    {
        return orgUuid;
    }

    public void setOrgUuid(String orgUuid)
    {
        this.orgUuid = orgUuid;
    }

    public String getOrgCode()
    {
        return orgCode;
    }

    public void setOrgCode(String orgCode)
    {
        this.orgCode = orgCode;
    }

    public String getOrgName()
    {
        return orgName;
    }

    public void setOrgName(String orgName)
    {
        this.orgName = orgName;
    }

    public Integer getUserNum()
    {
        return userNum;
    }

    public void setUserNum(Integer userNum)
    {
        this.userNum = userNum;
    }

    public Integer getDeviceNum()
    {
        return deviceNum;
    }

    public void setDeviceNum(Integer deviceNum)
    {
        this.deviceNum = deviceNum;
    }

    public Integer getExmobiAppNum()
    {
        return exmobiAppNum;
    }

    public void setExmobiAppNum(Integer exmobiAppNum)
    {
        this.exmobiAppNum = exmobiAppNum;
    }

    public Integer getLicenseNum()
    {
        return licenseNum;
    }

    public void setLicenseNum(Integer licenseNum)
    {
        this.licenseNum = licenseNum;
    }

    public Integer getUsedLicenseNum()
    {
        return usedLicenseNum;
    }

    public void setUsedLicenseNum(Integer usedLicenseNum)
    {
        this.usedLicenseNum = usedLicenseNum;
    }

    public Integer getOrgStatus()
    {
        return orgStatus;
    }

    public void setOrgStatus(Integer orgStatus)
    {
        this.orgStatus = orgStatus;
    }

}
